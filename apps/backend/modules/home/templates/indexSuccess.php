                <p>L'application web a été mise en place le 5 novembre 2012. 
                Depuis cette date, le protocole de recueil des données a évolué. Désormais les observations des espèces sont orientées vers 153 unités géographiques. 
                Dans chaque unité, une couleur permet de savoir pour chaque taxon, s'il a déjà été recemment observé ou s'il doit être recherché.</p>
                <p>Les outils nomades ont été déployés fin juin 2013. Quelques agents 'testeurs' les ont utilisés dès le mois de février 2013.</p>
                <p>L'application invertébrés a été déployée le 17 juillet 2013.</p>
                <p>Ce graphique évolue en temps réel (après chaque insertion de données dans la base, faire F5).</p>
